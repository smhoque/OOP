package Polymorphism;

public class ProgramCost{
	public int installment(int a, int b, int c, int d){
		int total = a + b+ c+ d; 
		return total;
		
	}
	public int installment(int a, int b, int c){
		int total = a + b +c; 
		return total;
	}
	public int installment(int a, int b){
		int total = a + b; 
		return total;
	}
		

}
